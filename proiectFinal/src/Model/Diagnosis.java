package Model;

public class Diagnosis {
	private String simptom;
	private String boli;
	
	public Diagnosis(){}

	public String getSimptom() {
		return simptom;
	}

	public void setSimptom(String simptom) {
		this.simptom = simptom;
	}

	public String getBoli() {
		return boli;
	}

	public void setBoli(String boli) {
		this.boli = boli;
	}
	
	
}
